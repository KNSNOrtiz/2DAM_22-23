    private void btn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn2ActionPerformed
        Object[] opciones = {"Mira sí", "Lo mismo no"};
        int respuesta = JOptionPane.showConfirmDialog(this, "¿Estás seguro de que quieres eliminar a ese alumno?");
        System.out.println("Resultado del JOptionPane: " + respuesta);
        switch(respuesta){
            case 0:
                JOptionPane.showOptionDialog(this, "¿Seguro seguro?", "Cuidao", JOptionPane.YES_NO_OPTION, respuesta, null, opciones, opciones[0]);
                break;
            case 1:
                JOptionPane.showMessageDialog(this, "Has pulsado que no");
            default:
                JOptionPane.showMessageDialog(this, "Has cancelado");
                break;

        }
    }//GEN-LAST:event_btn2ActionPerformed


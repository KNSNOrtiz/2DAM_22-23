    private void ImportarSerializable(){
        fichero = new File(txtImportar.getText());
        try{
            fis = new FileInputStream(fichero);
            ois = new ObjectInputStream(fis);
            listaAlumnos = (ArrayList <Alumno>)ois.readObject();
            ois.close();
            fis.close();
        } catch (FileNotFoundException ex){
            JOptionPane.showMessageDialog(null, "ERROR: No se ha podido abrir el fichero.");
        } catch (IOException ex){
            JOptionPane.showMessageDialog(null, "ERROR: No se ha podido leer el fichero.");
        } catch (ClassNotFoundException ex){
            JOptionPane.showMessageDialog(null, "ERROR: No se ha podido cargar la lista.");
        }
    }
    
    private void ImportarTexto(){
        fichero = new File(txtImportar.getText());
        String linea = "";
        try{
            fr = new FileReader(fichero);
            br = new BufferedReader(fr);
            while ((linea = br.readLine()) != null) {  
                try{
                    String[] datosAlumnos = linea.split("\\*");
                    Alumno alTmp = new Alumno(Integer.valueOf(datosAlumnos[0]), datosAlumnos[1], datosAlumnos[2], Double.valueOf(datosAlumnos[3]), Integer.valueOf(datosAlumnos[4]));
                    if (CtrlAlumnos.comprobarMatricula(alTmp.getNumMatr()) == 0) {
                       listaAlumnos.add(alTmp); 
                    }
                } catch (NumberFormatException ex){
                    JOptionPane.showMessageDialog(null, "Error de lectura en el fichero.");
                } catch (StringIndexOutOfBoundsException ex){
                    JOptionPane.showMessageDialog(null, "Error al leer datos del alumno.");
                }
                
            }
            fr.close();
            br.close();
            
        } catch (FileNotFoundException ex){
            JOptionPane.showMessageDialog(null, "ERROR: No se ha podido abrir el fichero.");
        } catch (IOException ex){
            JOptionPane.showMessageDialog(null, "ERROR: No se puede escribir en el fichero.");
        }       
    }
    private void ImportarXML(){
        fichero = new File(txtImportar.getText());
        try{
            builder = DocumentBuilderFactory.newInstance().newDocumentBuilder();
            doc = builder.parse(fichero);
            raiz = doc.getDocumentElement();
            getNodes(raiz);      
        } catch (ParserConfigurationException ex) {
            JOptionPane.showMessageDialog(null, "ERROR: No se ha podido cargar el parseador para leer.");
        } catch (SAXException ex) {
            JOptionPane.showMessageDialog(null, "ERROR: No se ha podido leer el documento." );
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "ERROR: No se ha podido acceder al fichero.");
        }
        
    }
    private void getNodes(Node nodo) {
    Alumno alTmp = new Alumno();    //  Trabajamos con un Alumno temporal "sin datos", que se crea cada vez que se llama a getNodes (es decir, al entrar en un nuevo nodo Alumno).
    System.out.println("Node: " + nodo.getNodeName());
    NodeList listaNodos = nodo.getChildNodes();
    for (int i = 0; i < listaNodos.getLength(); i++) {
        Node nodoActual = listaNodos.item(i);
        System.out.println(nodoActual.getNodeName());
        if (nodoActual.getNodeType() == Node.ELEMENT_NODE) {
                if (nodoActual.getTextContent() != "" && nodoActual.getTextContent() != null) {
                    switch(nodoActual.getNodeName()){
                        case "NumMatricula":
                            alTmp.setNumMatr(Integer.valueOf(nodoActual.getTextContent()));
                            break;
                        case "Nombre":
                            alTmp.setNombre(nodoActual.getTextContent());
                            break;
                        case "FechaNacimiento":
                            alTmp.setFnac(Alumno.StringToDate(nodoActual.getTextContent()));
                            break; 
                        case "NotaMedia":
                            alTmp.setNotaMedia(Double.valueOf(nodoActual.getTextContent()));
                            break;
                        case "Edad":
                            alTmp.setEdad(Integer.valueOf(nodoActual.getTextContent()));
                            if(CtrlAlumnos.comprobarMatricula(alTmp.getNumMatr()) == 0){
                              listaAlumnos.add(alTmp);    //  Se añade al alumno en esta parte, ya que es el último dato que se obtiene del alumno en el XML.  
                            }
                            break;
                }
            }  
            Element element = (Element) nodoActual;
            if (nodoActual.getNodeName().equals("Alumno")) {                    
                getNodes(element);               
            }

        }
    }
}
    

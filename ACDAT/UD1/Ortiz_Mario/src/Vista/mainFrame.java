     private void ExportarTexto(){
        fichero = new File(txtExportar.getText());
        try{
            fw = new FileWriter(fichero);
            bw = new BufferedWriter(fw);
            for(Alumno alTmp : listaAlumnos){
                bw.write(alTmp.toString() + "\n");
            }
            bw.close();
            fw.close();
            
        } catch (FileNotFoundException ex){
            JOptionPane.showMessageDialog(null, "ERROR: No se ha podido abrir el fichero.");
        } catch (IOException ex){
            JOptionPane.showMessageDialog(null, "ERROR: No se puede escribir en el fichero.");
        }
    }
     
     private void ExportarXML(){
        try{
            builder = DocumentBuilderFactory.newInstance().newDocumentBuilder();
            doc = builder.newDocument();
            raiz = doc.createElement("Alumnos");
            doc.appendChild(raiz);         
            for(Alumno alumno : listaAlumnos){
                nodo = doc.createElement("Alumno");
                Element nodoNombre = doc.createElement("Nombre");
                Element nodoMatricula = doc.createElement("NumMatricula");
                Element nodoFnac = doc.createElement("FechaNacimiento");
                Element nodoNota = doc.createElement("NotaMedia");
                Element nodoEdad = doc.createElement("Edad");
                nodoMatricula.setTextContent(String.valueOf(alumno.getNumMatr()));
                nodoNombre.setTextContent(alumno.getNombre());
                nodoFnac.setTextContent(Alumno.DateToString(alumno.getFnac()));
                nodoNota.setTextContent(String.valueOf(alumno.getNotaMedia()));
                nodoEdad.setTextContent(String.valueOf(alumno.getEdad()));
                nodo.appendChild(nodoMatricula);
                nodo.appendChild(nodoNombre);
                nodo.appendChild(nodoFnac);
                nodo.appendChild(nodoNota);
                nodo.appendChild(nodoEdad);
                raiz.appendChild(nodo);
            }
        } catch (ParserConfigurationException ex){
            JOptionPane.showMessageDialog(null, "ERROR DE PARSEO");
        }
        try {
           transform = TransformerFactory.newInstance().newTransformer();
           transform.setOutputProperty(OutputKeys.INDENT, "yes");
           Source source = new DOMSource(doc);
           String rutaFichero = txtExportar.getText();
           Result resultado = new StreamResult(new File(rutaFichero));
           transform.transform(source, resultado);
        } catch (TransformerConfigurationException ex) {
            JOptionPane.showMessageDialog(null, "ERROR AL CREAR EL TRANSFORMER");
        } catch (TransformerException ex) {
           JOptionPane.showMessageDialog(null, "ERROR AL CREAR EL DOCUMENTO XML");
        }
     }
    

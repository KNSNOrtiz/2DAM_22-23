    private void btnConsultarPersonasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarPersonasActionPerformed
        tablaPersonas.setRowCount(0);
        Session sesion = controlador.NewHibernateUtil.getSessionFactory().openSession();
        String hql = "FROM Persona";
        Query consulta = sesion.createQuery(hql);
        List<Persona> personas = consulta.list();   
        for (Persona persona : personas) {
            Object[] datos = {persona.getId(),persona.getNombre(),persona.getNumlibros()};
            tablaPersonas.addRow(datos);
        }
        sesion.close();
    }//GEN-LAST:event_btnConsultarPersonasActionPerformed


    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
        Session sesion = controlador.NewHibernateUtil.getSessionFactory().openSession();
        String hql = "FROM Usuarios AS u WHERE u.username = :user and u.password = :pass";
        
        Query consulta = sesion.createQuery(hql);
        consulta.setParameter("user", txtUsuario.getText());
        consulta.setParameter("pass", txtPass.getText());
        List<Usuarios> usuarios = consulta.list();
        if (usuarios.size() > 0) {
            JOptionPane.showMessageDialog(this, "Se ha iniciado sesión correctamente.");
        } else{
            JOptionPane.showMessageDialog(this, "El usuario no existe.");
        }
        sesion.close();
    }//GEN-LAST:event_btnIniciarSesionActionPerformed


    private void IniciarServer(){
        try {
            ss = new ServerSocket(1234);    //  Se inicia el socket del server a través de un puerto.
            socket = ss.accept();               //  Se obtiene un socket al indicar que el server acepta conexiones.
            entrada = new DataInputStream(socket.getInputStream()); //  Obtenemos un flujo de entrada para recibir mensajes.
            mensaje = entrada.readUTF();    //  Indicamos que el servidor lee texto.
            txtLog.append(mensaje + "\n");  //  Cada vez que se reciba un mensaje desde el cliente, se mandará por pantalla.
            
            
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error al iniciar el servidor.");
        }
        
    }
    

